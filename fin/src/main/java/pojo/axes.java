package pojo;

public class axes {

	public String s1,s2,s3,s4;
	public axes(){}
	public axes(String s1, String s2, String s3, String s4) {
		this.s1=s1;
		this.s2=s2;
		this.s3=s3;
		this.s4=s4;
		
	}

	public String getDate1()
	{
		return s1;
	}
	public String getRead1()
	{
		return s2;
	}
	public String getDate2()
	{
		return s3;
	}
	public String getRead2()
	{
		return s4;
	}

}
